export function translateIssue(t, ruleId, fallbackTitle, fallbackDesc) {
  const isEn = String(t('locale')).startsWith('en');
  if (isEn && ruleId && ISSUE_EN[ruleId]) {
    return { title: ISSUE_EN[ruleId].title, desc: ISSUE_EN[ruleId].desc };
  }
  return { title: fallbackTitle, desc: fallbackDesc };
}
